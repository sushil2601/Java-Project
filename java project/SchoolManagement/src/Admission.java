    void fillTable(String classname)
    {
        Vector heading=new Vector();
        heading.add("fee_name");
        heading.add("fee");
        try
        {
            String query="select * from tablehead,classwisedetail where tablehead.fee_id=classwisedetail.fee_id and classwisedetail.class_name=?";
          
            PreparedStatement ps=con.prepareStatement(query);
            ps.setString(1,classname);
            ResultSet rs=ps.executeQuery();
            Vector data=new Vector();
            while(rs.next())
            {
                Vector row=new Vector();
                String feeid=rs.getString("fee_name");
                String fee=rs.getString("fee");
                row.add(feeid);
                row.add(fee);
                data.add(row);
            }
            DefaultTableModel d=new DefaultTableModel(data,heading);
            jTable1.setModel(d);
            

            TableModel tm=jTable1.getModel();
            int sum=0;
            
            int count=jTable1.getRowCount();
            for(int i=0;i<count;i++)
            {
                String sm=(String)tm.getValueAt(i, 1);
              sum+=Integer.parseInt(sm); 
            }
            
            txtTotal.setText(String.valueOf(sum));
            
            
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
    
   

