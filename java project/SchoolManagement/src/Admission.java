    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String studentid=txtstudentid.getText();
       try
       {
           String query="select studentname,fathername,dob,gender from studentdata where studentid=?";
           PreparedStatement ps=con.prepareStatement(query);
           ps.setString(1,studentid);
           ResultSet rs=ps.executeQuery();
           while(rs.next())
           {
               String name=rs.getString("studentname");
               String fname=rs.getString("fathername");
               String dob=rs.getString("dob");
               String gender=rs.getString("gender");
           
           txtstudentname.setText(name);
           txtfathername.setText(fname);
           txtdob.setText(dob);
           if(gender.equalsIgnoreCase("male"))
           {
               rmale.setSelected(true);
           }
           else
           {
               rfemale.setSelected(true);
           }
           }
         
       }
       catch(Exception e)
       {
           e.printStackTrace();
       }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String studentid=txtstudentid.getText();
        String classname=(String)comboclassname.getSelectedItem();
        String totalamount=txtTotal.getText();
        String date=txtdate.getText();
        String frequency=(String)comfrequency.getSelectedItem();
        String paymentdetail=txapaymentdetail.getText();
        String paymentmode="";
        if(rcheque.isSelected())
        {
            paymentmode="cheque";
        }
        else
        {
            paymentmode="cash";
        }
        String month=(String)commonth.getSelectedItem();
        String year=txtyear.getText();
       try
       {
           String query="insert into studentfee(studentid,class_name,total_amount,date,frequency,paymentmode,paymentdetail,month,year) values(?,?,?,?,?,?,?,?,?)";
           PreparedStatement ps=con.prepareStatement(query);
           ps.setString(1,studentid);
           ps.setString(2,classname);
           ps.setString(3,totalamount);
           ps.setString(4,date);
           ps.setString(5,frequency);
           ps.setString(6,paymentmode);
           ps.setString(7,paymentdetail);
           ps.setString(8,month);
           ps.setString(9,year);
           
           int count=ps.executeUpdate();
           
           JOptionPane.showMessageDialog(this,count+"save");
       }
       catch(Exception e)
       {
           e.printStackTrace();
       }
    }//GEN-LAST:event_jButton2ActionPerformed

