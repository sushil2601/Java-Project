       String feename=txtfeename.getText();
       String feestatus="";
       if(rmonthly.isSelected())
       {
           feestatus="monthly";
       }
       else
       {
           feestatus="Annualy";
       }
       try
       {
           String query="insert into tablehead (fee_name,frequency) values(?,?)";
           PreparedStatement ps=con.prepareStatement(query);
           ps.setString(1,feename);
           ps.setString(2,feestatus);
             
           
           int count=ps.executeUpdate();
           JOptionPane.showMessageDialog(this,count+"fee inserted ");
           fillTable();
       }
       catch(Exception e)
       {
           e.printStackTrace();
       }
      
