    void fillTable()
    {
        Vector heading=new Vector();
        heading.add("fee_id");
        heading.add("fee_name");
        heading.add("frequency");
        try
        {
            String query="select * from tablehead";
            PreparedStatement ps=con.prepareStatement(query);
            ResultSet rs=ps.executeQuery();
            Vector data=new Vector();
            
            while(rs.next())
            {
                Vector row=new Vector();
                String feeid= rs.getString("fee_id");
                String feename=rs.getString("fee_name");
                String frequency=rs.getString("frequency");
                row.add(feeid);
                row.add(feename);
                row.add(frequency);
                data.add(row);
            }
            DefaultTableModel d=new DefaultTableModel(data,heading);
            jTable1.setModel(d);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }

