    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String studentname=txtstudentname.getText();
        String fathername=txtfathername.getText();
        String mothername=txtmothername.getText();
        String dob=txtdob.getText();
         String gender="";
        if(rmale.isSelected())
        {
            gender="male";
            
        }
        else
        {
            gender="female";
        }
        String contact1=txtcontact1.getText();
        String contact2=txtcontact2.getText();
        String address=txaaddress.getText();
       
        
        try
        {
            String query="insert into studentdata(studentname,fathername,mothername,dob,gender,contact1,contact2,address) values(?,?,?,?,?,?,?,?)";
            PreparedStatement ps=con.prepareStatement(query);
                 
            ps.setString(1,studentname);
            ps.setString(2,fathername);
            ps.setString(3,mothername);
            ps.setString(4,dob);
            ps.setString(5,gender);
            ps.setString(6,contact1);
            ps.setString(7,contact2);
            ps.setString(8,address);
            
            int count=ps.executeUpdate();
            JOptionPane.showMessageDialog(this,count+"data inserted");
            fillTable();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

