    void fillTable()
    {
        Vector heading=new Vector();
        heading.add("Student_id");
        heading.add("Student_name");
        heading.add("Father's_name");
        heading.add("Mother's_name");
        heading.add("D.O.B");
        heading.add("Gender");
        heading.add("Contact1");
        heading.add("Contact2");
        heading.add("Address");
        
        try
        {
            String query="select * from studentdata";
            PreparedStatement ps=con.prepareStatement(query);
            ResultSet rs=ps.executeQuery();
            Vector data=new Vector();
            while(rs.next())
            {
                Vector row=new Vector();
                String studentid=rs.getString("studentid");
                String studentname=rs.getString("studentname");
                String fathername=rs.getString("fathername");
                String mothername=rs.getString("mothername");
                String dob=rs.getString("dob");
                String contact1=rs.getString("contact1");
                String contact2=rs.getString("contact2");
                String address=rs.getString("address");
                
                row.add(studentid);
                row.add(studentname);
                row.add(fathername);
                row.add(mothername);
                row.add(dob);
                row.add(contact1);
                row.add(contact2);
                row.add(address);
                data.add(row);
                
            }
            DefaultTableModel d=new DefaultTableModel(data,heading);
            jTable1.setModel(d);
                    
            
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
        
    }

